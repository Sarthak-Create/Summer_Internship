#include<iostream>
using namespace std;
int main() {
    int n1 = 15; //decimal
    int n2 = 017; //octal
    int n3 = 0x0F; //hexadecimal
    int n4 = 0b00001111; //binary

    cout<<"Number1 : "<<n1<<endl;
    cout<<"Number2 : "<<n2<<endl;
    cout<<"Number3 : "<<n3<<endl;
    cout<<"Number4 : "<<n4<<endl;

    return 0;
}
